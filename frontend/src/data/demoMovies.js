export const allDemoMovies = [...demoMovies.now, ...demoMovies.soon]

export function getDemoMovieById(movieId) {
    return allDemoMovies.find(movie => String(movie.movieId) === String(movieId))
}

export function mergeWithDemoMovies(apiMovies = [], filter = "now", minimum = 10) {
    const sourceMovies = Array.isArray(apiMovies) ? apiMovies : [];
    const seenNames = new Set(sourceMovies.map(movie => String(movie.movieName || "").trim().toLowerCase()).filter(Boolean));
    const mergedMovies = [...sourceMovies];

    for (const movie of demoMovies[filter] || []) {
        const movieName = String(movie.movieName || "").trim().toLowerCase();
        if (!movieName || seenNames.has(movieName)) {
            continue;
        }
        mergedMovies.push(movie);
        seenNames.add(movieName);
        if (mergedMovies.length >= minimum) {
            break;
        }
    }

    return mergedMovies;
}
